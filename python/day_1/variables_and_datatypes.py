# variables and data types

# integer
x = 10
y = 20
z = x + y
print(z)
print(type(z))

# float 
x = 10.10
y = 20.20
z = x + y
print(z)
print(type(z))

# string
x = "hello world"
print(x)
print(type(x))

# boolean
x = True
print(x)
print(type(x))

# type conversion (string to number)
num1 = "10"
num2 = 20
print(int(num1) + num2)


